def main():
    # Printing
    print("Hello, World!")
    print("Today's date:", "2025", "07", "22", sep="-")

    # Variables
    first_name = "Vallarasu"
    last_name = "Kanthasamy"
    age = 24
    is_coding = True

    # Print all variables
    print("Name:", first_name, last_name, "| Age:", age, "| Coding:", is_coding)

    # Print data types
    print("Type of first_name:", type(first_name))
    print("Type of age:", type(age))
    print("Type of is_coding:", type(is_coding))

if __name__ == '__main__':
    main()
